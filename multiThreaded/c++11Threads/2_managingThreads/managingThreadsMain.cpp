#include <iostream>
#include <thread>

using namespace std;

void sayHello()
{
	cout << "sayHello - hello from thread" << endl;
}

// =============================================================================================================================
// -----------
// main
// -----------
// =============================================================================================================================

int main(int argc, char** argv)
{	
	cout << "main - start" << endl;
	thread t1(sayHello);
	t1.join();
	
	cout << "main - end" << endl;
	return 0;
}

#include <iostream>

using namespace std;

class MyInnerClass
{
public:
	MyInnerClass(const string& str1, const string& str2)
		: m_str1(str1)
		, m_str2(str2)
	{
		cout << "MyInnerClass::MyInnerClass - set m_str1:" << m_str1 << ", m_str2:" << m_str2 << endl;
	}

	~MyInnerClass()
	{
		cout << "MyInnerClass::~MyInnerClass" << endl;
	}

	string m_str1;
	string m_str2;
};

class MyClass
{
public:
	MyClass()
		: m_innerClass(m_str1, m_str2)
	{
		cout << "MyClass::MyClass - m_str1:" << m_str1 << ", m_str2:" << m_str2 << endl;
	}
	
	~MyClass()
	{
		cout << "MyClass::~MyClass" << endl;
	}

	friend ostream& operator<<(ostream& out, const MyClass& o);

private:
	const string m_str1 = "const_string";
	string m_str2 = "non_const_string";
	MyInnerClass m_innerClass;
};

ostream& operator<<(ostream& out, const MyClass& o)
{
	out << "m_str1:" << o.m_str1 << ", m_str2:" << o.m_str2 << endl;
	return out;
}

// ===================================================================================================================================================================
// ===================================================================================================================================================================
// main
// ===================================================================================================================================================================
// ===================================================================================================================================================================

int main(int argc, char** argv)
{

	std::cout << "main - start" << std::endl;
	
	MyClass obj;
	cout << "main - after creating MyClass object on the main stack it has:" << obj;

	std::cout << "main - end" << std::endl;
	return 0;
}



